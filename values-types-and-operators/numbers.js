/*
 * Leave Report
 * Write an expression that calculate the average leave day of employees in this year
 */

var annualLeave = 30
var BobRD = 12
var AliceRD = 28
var TomRD = 19
var average = (3*annualLeave-BobRD-AliceRD-TomRD)/3

exports.average = average
exports.annualLeave = annualLeave
exports.BobRD = BobRD
exports.AliceRD = AliceRD
exports.TomRD = TomRD
